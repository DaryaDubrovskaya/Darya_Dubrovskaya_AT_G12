package pretask;

public class MathOperatorsExample {
    public static void main(String[] args) {
        int e1 = 6;
        int e2 = 8;

        int sum = e1 + e2;
        int sub = e1 - e2;
        int mult = e1 * e2;
        double div = (double) e1 / e2;

        System.out.println("sum = " + sum + "\nsub = " + sub + "\nmult = " + mult + "\ndiv = " + div);
    }
}
